import tkinter as tk


def show_selected_items():
    selected_items = listbox.curselection()
    print([listbox.get(i) for i in selected_items])


def filter_items():
    search_term = search_entry.get().lower()
    listbox.delete(0, tk.END)
    for item in LICENSES.keys():
        if search_term in item.lower():
            listbox.insert(tk.END, item)
    color_rows()


def on_key_press(event):
    filter_items()


def color_rows():
    for i in range(listbox.size()):
        color = 'white' if i % 2 == 0 else '#f0f0f0'
        listbox.itemconfig(i, {'bg':color})

# Main
root = tk.Tk()
root.title("Listbox")

# Container
frame = tk.Frame(root)
frame.pack(padx=10, pady=10)

# Scrollbar
scrollbar = tk.Scrollbar(frame, orient=tk.VERTICAL)

# Listbox
listbox = tk.Listbox(frame, selectmode=tk.MULTIPLE, width=100, height=45, yscrollcommand=scrollbar.set)
listbox.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)

# Configure scrollbar
scrollbar.config(command=listbox.yview)
scrollbar.pack(side=tk.RIGHT, fill=tk.Y)

# Add items
for item in LICENSES.keys():
    listbox.insert(tk.END, item)

# Search Entry
search_label = tk.Label(root, text="Search")
search_label.pack(padx=10, anchor=tk.W)
search_entry = tk.Entry(root)
search_entry.pack(padx=10, fill=tk.X, pady=5)
search_entry.bind("<KeyPress>", on_key_press)

